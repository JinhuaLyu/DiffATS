def get_config():
    config = ml_collections.ConfigDict()

    config.seed = 1234
    config.pred = 'noise_pred'

    # 1000 epochs x 10000 train x batch 32 -> 312000 steps.
    # 120 tokens (vs 192 in v1) -> ~2.5x cheaper attention -> ~2-3h for 1000 epochs.
    config.train = d(
        n_epochs=1000,
        n_steps=312_000,
        batch_size=32,
        time_budget_hours=1e9,
        mode='cond',
        log_interval=200,
        eval_interval=20_000,
        save_interval=40_000,
    )

    config.optimizer = d(
        name='adamw',
        lr=1e-4,
        weight_decay=0.03,
        betas=(0.99, 0.99),
    )

    config.lr_scheduler = d(
        name='customized',
        warmup_steps=-1
        # T_max=312_000,
        # eta_min=1e-6,
    )

    # Field 240 x 1024 with B_t=16, B_x=32, M_x=4, low_freqs=64:
    #   n_t = 240/16 = 15; n_x_macro = 1024/(32*4) = 8 -> tokens = 15*8 = 120
    #   feature_dim = M_x * low_freqs = 4 * 64 = 256
    #   total floats per sample = 120 * 256 = 30,720  (matches v1, same compression)
    # Same block layout as reaction v3 (B_t=16, B_x=32) + cosine LR from scratch.
